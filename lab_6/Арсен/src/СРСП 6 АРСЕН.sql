-- 1. Общая сумма платежей на i-ю дату
DECLARE @Дата DATE = '2024-03-25';
SELECT 
    Дата, 
    SUM(СуммаПлатежа) AS ОбщаяСумма
FROM ПлановыеПлатежи
WHERE Дата = @Дата
GROUP BY Дата;

-- 2. Количество различных банков (вместо городов)
SELECT 
    COUNT(DISTINCT Наименование) AS КоличествоБанков
FROM Банки
WHERE Адрес IS NOT NULL;

-- 3. Наименьший платеж для каждого природопользователя
SELECT
    п.Наименование AS Природопользователь,
    MIN(пп.СуммаПлатежа) AS МинимальныйПлатеж
FROM ПлановыеПлатежи пп
JOIN Природопользователи п ON пп.БИН = п.БИН
GROUP BY п.Наименование;

-- 4. Природопользователи в алфавитном порядке
SELECT DISTINCT Наименование
FROM Природопользователи
ORDER BY Наименование;

-- 5. Штраф за превышение лимитов (вместо комиссионных 12%)
SELECT
    уб.НомерРазрешения,
    уб.ОбъемВыбросовВУсловныхТоннах,
    л.ОбъемВУсловныхТоннах AS Лимит,
    (уб.ОбъемВыбросовВУсловныхТоннах - л.ОбъемВУсловныхТоннах) * т.СтавкаЗаУсловнуюТонну * 2 AS Штраф
FROM УчетВыбросов уб
JOIN Лимиты л ON уб.НомерРазрешения = л.НомерРазрешения 
    AND уб.КодВещества = л.КодВещества 
    AND уб.КодТипаВыброса = л.КодТипаВыброса
JOIN Тарифы т ON уб.КодВещества = т.КодВещества AND уб.КодТипаВыброса = т.КодТипаВыброса
WHERE уб.ОбъемВыбросовВУсловныхТоннах > л.ОбъемВУсловныхТоннах;

-- 6. Количество разрешений у каждого природопользователя
SELECT
    п.Наименование AS Природопользователь,
    COUNT(р.НомерРазрешения) AS КоличествоРазрешений
FROM Природопользователи п
LEFT JOIN Разрешения р ON п.БИН = р.БИН
GROUP BY п.Наименование;