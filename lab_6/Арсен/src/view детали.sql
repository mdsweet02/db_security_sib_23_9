-- Детальная информация о разрешениях с лимитами и тарифами
CREATE VIEW ДеталиРазрешений AS
SELECT  
    р.НомерРазрешения,
    п.Наименование AS Природопользователь,
    п.БИН,
    р.ДатаПолучения,
    р.ПериодДействия,
    DATEADD(MONTH, р.ПериодДействия, р.ДатаПолучения) AS ДатаОкончания,
    тв.Наименование AS ТипВыброса,
    в.Наименование AS Вещество,
    л.ОбъемВУсловныхТоннах AS Лимит,
    т.СтавкаЗаУсловнуюТонну AS Тариф,
    (л.ОбъемВУсловныхТоннах * т.СтавкаЗаУсловнуюТонну) AS ПлановыйПлатеж
FROM Разрешения р
JOIN Природопользователи п ON р.БИН = п.БИН
JOIN Лимиты л ON р.НомерРазрешения = л.НомерРазрешения
JOIN Вещества в ON л.КодВещества = в.КодВещества
JOIN ТипыВыбросов тв ON л.КодТипаВыброса = тв.КодТипаВыброса
JOIN Тарифы т ON в.КодВещества = т.КодВещества AND тв.КодТипаВыброса = т.КодТипаВыброса
WITH CHECK OPTION;
GO