CREATE PROCEDURE Вещества_ПоТипуВыброса
    @КодТипаВыброса INT  -- Параметр: код типа выброса
AS
BEGIN
    -- Выбираем все вредные вещества для указанного типа выбросов
    SELECT DISTINCT
        в.КодВещества,
        в.Наименование AS Вещество,
        в.КлассОпасности,
        тв.Наименование AS ТипВыброса,
        COUNT(л.НомерРазрешения) AS КоличествоРазрешений,
        SUM(л.ОбъемВУсловныхТоннах) AS ОбщийЛимит
    FROM Лимиты л
    JOIN Вещества в ON л.КодВещества = в.КодВещества
    JOIN ТипыВыбросов тв ON л.КодТипаВыброса = тв.КодТипаВыброса
    WHERE л.КодТипаВыброса = @КодТипаВыброса
    GROUP BY в.КодВещества, в.Наименование, в.КлассОпасности, тв.Наименование
    ORDER BY в.Наименование;
    
    PRINT 'Выбраны вещества для типа выброса: ' + CAST(@КодТипаВыброса AS VARCHAR);
END;
GO