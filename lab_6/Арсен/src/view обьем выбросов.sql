-- 1. Общий объем выбросов каждого из типов выбросов загрязняющих веществ в условных тоннах
CREATE VIEW ОбъемВыбросовПоТипам AS
SELECT  
    тв.Наименование AS ТипВыброса,
    SUM(уб.ОбъемВыбросовВУсловныхТоннах) AS ОбщийОбъем,
    COUNT(уб.КодЗаписи) AS КоличествоЗаписей
FROM УчетВыбросов уб
JOIN ТипыВыбросов тв ON уб.КодТипаВыброса = тв.КодТипаВыброса
GROUP BY тв.КодТипаВыброса, тв.Наименование;
GO

-- 2. Список банков в которых обслуживаются природопользователи, стоящие на учете в 1-ом налоговом комитете
CREATE VIEW БанкиПервогоКомитета AS
SELECT DISTINCT
    б.КодБанка,
    б.Наименование AS Банк,
    б.Адрес AS АдресБанка,
    п.Наименование AS Природопользователь,
    нк.Наименование AS НалоговыйКомитет
FROM Природопользователи п
JOIN Банки б ON п.КодБанка = б.КодБанка
JOIN НалоговыеКомитеты нк ON п.КодНалоговогоКомитета = нк.КодКомитета
WHERE п.КодНалоговогоКомитета = 1;
GO

-- 3. Вычислить объем выбросов каждого природопользователя в реальных тоннах
CREATE VIEW ОбъемВыбросовПоПриродопользователям AS
SELECT  
    п.БИН,
    п.Наименование AS Природопользователь,
    тв.Наименование AS ТипВыброса,
    в.Наименование AS Вещество,
    SUM(уб.ОбъемВыбросовВУсловныхТоннах) AS ОбъемУсловныхТонн,
    SUM(уб.ОбъемВыбросовВУсловныхТоннах * в.КоэффициентПриведения) AS ОбъемРеальныхТонн
FROM УчетВыбросов уб
JOIN Разрешения р ON уб.НомерРазрешения = р.НомерРазрешения
JOIN Природопользователи п ON р.БИН = п.БИН
JOIN Вещества в ON уб.КодВещества = в.КодВещества
JOIN ТипыВыбросов тв ON уб.КодТипаВыброса = тв.КодТипаВыброса
GROUP BY п.БИН, п.Наименование, тв.Наименование, в.Наименование;
GO

-- 4. Список природопользователей, у которых окончился срок действия разрешения
CREATE VIEW ПросроченныеРазрешения AS
SELECT  
    р.НомерРазрешения,
    п.Наименование AS Природопользователь,
    р.ДатаПолучения,
    DATEADD(MONTH, р.ПериодДействия, р.ДатаПолучения) AS ДатаОкончания,
    DATEDIFF(DAY, GETDATE(), DATEADD(MONTH, р.ПериодДействия, р.ДатаПолучения)) AS ДнейДоОкончания
FROM Разрешения р
JOIN Природопользователи п ON р.БИН = п.БИН
WHERE DATEADD(MONTH, р.ПериодДействия, р.ДатаПолучения) < GETDATE();
GO