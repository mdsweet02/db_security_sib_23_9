-- Минимальный рабочий триггер
CREATE TRIGGER TR_УчетВыбросов_INSERT_Minimal
ON УчетВыбросов
AFTER INSERT
AS
BEGIN
    PRINT 'Триггер INSERT на таблице УчетВыбросов успешно сработал!';
END;
GO


INSERT INTO УчетВыбросов (
    НомерРазрешения,
    Дата,
    Квартал,
    Год,
    КодВещества,
    ОбъемВыбросовВУсловныхТоннах,
    КодТипаВыброса
)
VALUES (
    'РАЗ-2024-001',    -- Существующий номер разрешения
    '2024-02-05',      -- Дата
    1,                 -- Квартал
    2024,              -- Год
    1,                 -- Код вещества
    125.50,            -- Объем выбросов
    1                  -- Тип выброса
);

-- Проверяем
