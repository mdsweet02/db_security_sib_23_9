CREATE TRIGGER trg_УчетВыбросов_Update
ON УчетВыбросов
AFTER UPDATE
AS
BEGIN
    -- Проверяем, не превышены ли лимиты
    IF EXISTS (
        SELECT 1 
        FROM inserted i
        JOIN Лимиты л ON i.НомерРазрешения = л.НомерРазрешения 
                      AND i.КодВещества = л.КодВещества 
                      AND i.КодТипаВыброса = л.КодТипаВыброса
        WHERE i.ОбъемВыбросовВУсловныхТоннах > л.ОбъемВУсловныхТоннах
    )
    BEGIN
        PRINT 'ВНИМАНИЕ: Объем выбросов превышает установленные лимиты!';
        
        -- Записываем информацию о превышении в журнал
        IF NOT EXISTS (SELECT * FROM sys.tables WHERE name = 'ЖурналПревышений')
        CREATE TABLE ЖурналПревышений (
            ID INT IDENTITY(1,1) PRIMARY KEY,
            НомерРазрешения NVARCHAR(50),
            КодВещества INT,
            КодТипаВыброса INT,
            ОбъемВыбросов DECIMAL(12,3),
            УстановленныйЛимит DECIMAL(12,3),
            Превышение DECIMAL(12,3),
            ДатаПревышения DATETIME DEFAULT GETDATE()
        );
        
        INSERT INTO ЖурналПревышений (НомерРазрешения, КодВещества, КодТипаВыброса, ОбъемВыбросов, УстановленныйЛимит, Превышение)
        SELECT 
            i.НомерРазрешения,
            i.КодВещества,
            i.КодТипаВыброса,
            i.ОбъемВыбросовВУсловныхТоннах,
            л.ОбъемВУсловныхТоннах,
            (i.ОбъемВыбросовВУсловныхТоннах - л.ОбъемВУсловныхТоннах)
        FROM inserted i
        JOIN Лимиты л ON i.НомерРазрешения = л.НомерРазрешения 
                      AND i.КодВещества = л.КодВещества 
                      AND i.КодТипаВыброса = л.КодТипаВыброса
        WHERE i.ОбъемВыбросовВУсловныхТоннах > л.ОбъемВУсловныхТоннах;
    END;
END;
GO