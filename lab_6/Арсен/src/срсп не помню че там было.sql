CREATE PROCEDURE КоличествоРазрешений_НаДату
    @ДатаПолучения DATE  -- Параметр: конкретная дата
AS
BEGIN
    -- Считаем количество природопользователей, получивших разрешение в указанную дату
    SELECT 
        @ДатаПолучения AS ЗапрошеннаяДата,
        COUNT(*) AS ВсегоРазрешений,
        COUNT(DISTINCT БИН) AS УникальныхПриродопользователей,
        STRING_AGG(НомерРазрешения, ', ') AS СписокРазрешений,
        SUM(СуммаНалога) AS ОбщаяСуммаНалогов,
        AVG(ПериодДействия) AS СреднийПериодДействия
    FROM Разрешения
    WHERE ДатаПолучения = @ДатаПолучения;
    
    PRINT 'Статистика по разрешениям на дату: ' + CONVERT(VARCHAR, @ДатаПолучения, 104);
END;
GO