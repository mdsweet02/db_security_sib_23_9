CREATE PROCEDURE sp_ПолучитьРазрешенияПользователя
    @БИН CHAR(12)
AS
BEGIN
    SELECT 
        р.НомерРазрешения,
        р.ДатаПолучения,
        р.ПериодДействия,
        р.ДатаОкончания,
        SUM(л.ПлановыйПлатеж) AS ОбщаяСуммаПлатежей
    FROM Разрешения р
    LEFT JOIN Лимиты л ON р.НомерРазрешения = л.НомерРазрешения
    WHERE р.БИН = @БИН
    GROUP BY р.НомерРазрешения, р.ДатаПолучения, р.ПериодДействия, р.ДатаОкончания;
    
    PRINT 'Найдено разрешений: ' + CAST(@@ROWCOUNT AS NVARCHAR(10));
END;
GO