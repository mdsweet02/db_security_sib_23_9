-- Финансовый отчет по платежам
CREATE VIEW ФинансовыйОтчет AS
SELECT  
    пп.НомерДокумента,
    п.Наименование AS Природопользователь,
    пп.Дата,
    пп.Квартал,
    пп.Год,
    пп.СуммаПлатежа,
    б.Наименование AS Банк,
    п.ЛицевойСчетВБанке
FROM ПлановыеПлатежи пп
JOIN Природопользователи п ON пп.БИН = п.БИН
JOIN Банки б ON п.КодБанка = б.КодБанка;
GO

-- Текущие выбросы с превышением лимитов
CREATE VIEW ПревышенияЛимитов AS
SELECT  
    уб.НомерРазрешения,
    п.Наименование AS Природопользователь,
    в.Наименование AS Вещество,
    тв.Наименование AS ТипВыброса,
    л.ОбъемВУсловныхТоннах AS УстановленныйЛимит,
    SUM(уб.ОбъемВыбросовВУсловныхТоннах) AS ФактическийВыброс,
    (SUM(уб.ОбъемВыбросовВУсловныхТоннах) - л.ОбъемВУсловныхТоннах) AS Превышение
FROM УчетВыбросов уб
JOIN Разрешения р ON уб.НомерРазрешения = р.НомерРазрешения
JOIN Природопользователи п ON р.БИН = п.БИН
JOIN Вещества в ON уб.КодВещества = в.КодВещества
JOIN ТипыВыбросов тв ON уб.КодТипаВыброса = тв.КодТипаВыброса
JOIN Лимиты л ON уб.НомерРазрешения = л.НомерРазрешения 
    AND уб.КодВещества = л.КодВещества 
    AND уб.КодТипаВыброса = л.КодТипаВыброса
GROUP BY уб.НомерРазрешения, п.Наименование, в.Наименование, тв.Наименование, л.ОбъемВУсловныхТоннах
HAVING SUM(уб.ОбъемВыбросовВУсловныхТоннах) > л.ОбъемВУсловныхТоннах;
GO