-- 2. Список банков, в которых обслуживаются природопользователи, стоящие на учете в 1-ом налоговом комитете
DECLARE @КодКомитета INT = 1; -- I-ый комитет

SELECT DISTINCT
    б.КодБанка,
    б.Наименование AS Банк,
    б.Адрес,
    COUNT(DISTINCT п.БИН) AS КоличествоКлиентов,
    STRING_AGG(п.Наименование, ', ') AS Клиенты
FROM Банки б
JOIN Природопользователи п ON б.КодБанка = п.КодБанка
WHERE п.КодНалоговогоКомитета = @КодКомитета
GROUP BY б.КодБанка, б.Наименование, б.Адрес
ORDER BY б.Наименование;