-- Создание представления ФинансовыйОтчет
CREATE VIEW ФинансовыйОтчет AS
SELECT 
    'ДОК-' + CAST(ROW_NUMBER() OVER (ORDER BY Дата) AS NVARCHAR(10)) AS НомерДокумента,
    п.Наименование AS Природопользователь,
    GETDATE() AS Дата,
    DATEPART(QUARTER, GETDATE()) AS Квартал,
    YEAR(GETDATE()) AS Год,
    0.00 AS СуммаПлатежа,  -- Заглушка
    'Банк' AS Банк,        -- Заглушка
    '00000000000000000000' AS ЛицевойСчетВБанке  -- Заглушка
FROM Природопользователи п;