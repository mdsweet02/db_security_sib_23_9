-- 4. Типы выбросов 1-го природопользователя
DECLARE @БИН_Природопользователя BIGINT = 123456789012; -- I-ый природопользователь

SELECT DISTINCT
    тв.КодТипаВыброса,
    тв.Наименование AS ТипВыброса,
    COUNT(DISTINCT уб.КодЗаписи) AS КоличествоЗаписей,
    COUNT(DISTINCT в.КодВещества) AS КоличествоВеществ,
    MIN(уб.Дата) AS ПерваяЗапись,
    MAX(уб.Дата) AS ПоследняяЗапись
FROM УчетВыбросов уб
JOIN Разрешения р ON уб.НомерРазрешения = р.НомерРазрешения
JOIN Природопользователи п ON р.БИН = п.БИН
JOIN ТипыВыбросов тв ON уб.КодТипаВыброса = тв.КодТипаВыброса
JOIN Вещества в ON уб.КодВещества = в.КодВещества
WHERE п.БИН = @БИН_Природопользователя
GROUP BY тв.КодТипаВыброса, тв.Наименование
ORDER BY тв.Наименование;