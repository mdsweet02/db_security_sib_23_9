CREATE PROCEDURE Разрешения_ПериодБольше3Месяцев
AS
BEGIN
    -- Выбираем все разрешения, где период действия больше 3 месяцев
    SELECT 
        НомерРазрешения,
        БИН,
        ДатаПолучения,
        ПериодДействия AS ПериодДействия_в_месяцах,
        DATEADD(MONTH, ПериодДействия, ДатаПолучения) AS ДатаОкончания,
        СуммаНалога
    FROM Разрешения
    WHERE ПериодДействия > 3
    ORDER BY ПериодДействия DESC;
    
    PRINT 'Выбраны разрешения с периодом действия более 3 месяцев';
END;
GO