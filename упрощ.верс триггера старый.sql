-- Упрощенная версия триггера
CREATE TRIGGER TR_УчетВыбросов_INSERT_Simple
ON УчетВыбросов
AFTER INSERT
AS
BEGIN
    SET NOCOUNT ON;
    
    PRINT 'Триггер INSERT для УчетВыбросов сработал!';
    
    -- Автоматически создаем платежи для всех добавленных записей
    INSERT INTO ФинансовыйОтчет (
        НомерДокумента,
        Природопользователь,
        Дата,
        Квартал,
        Год,
        СуммаПлатежа,
        Банк,
        ЛицевойСчетВБанке
    )
    SELECT 
        'АВТО-' + FORMAT(GETDATE(), 'yyyyMMddHHmmss') + '-' + CAST(i.КодЗаписи AS NVARCHAR(10)),
        'Автоматический платеж',  -- Заглушка вместо природопользователя
        i.Дата,
        i.Квартал,
        i.Год,
        i.ОбъемВыбросовВУсловныхТоннах * 1000.00,  -- Простой расчет
        'Автобанк',              -- Заглушка для банка
        '00000000000000000000'   -- Заглушка для счета
    FROM inserted i;
    
    PRINT 'Создано автоматических платежей: ' + CAST(@@ROWCOUNT AS NVARCHAR(10));
END;
GO