-- БЛОК 3: «I-ое» разрешение с лимитами (исправленный)
CREATE VIEW Разрешение_С_Лимитами AS
SELECT 
    р.НомерРазрешения,
    р.БИН,
    р.ДатаПолучения,
    р.ПериодДействия,
    л.КодВещества,
    л.ОбъемВУсловныхТоннах AS Лимит
FROM Разрешения р
LEFT JOIN Лимиты л ON р.НомерРазрешения = л.НомерРазрешения
WHERE р.НомерРазрешения = 'I-ое';