CREATE TRIGGER trg_УчетВыбросов_Insert
ON УчетВыбросов
AFTER INSERT
AS
BEGIN
    -- Обновляем сумму платежа на основе фактических выбросов
    UPDATE пп
    SET СуммаПлатежа = (
        SELECT SUM(уб.ОбъемВыбросовВУсловныхТоннах * т.СтавкаЗаУсловнуюТонну)
        FROM УчетВыбросов уб
        JOIN Разрешения р ON уб.НомерРазрешения = р.НомерРазрешения
        JOIN Вещества в ON уб.КодВещества = в.КодВещества
        JOIN Тарифы т ON уб.КодВещества = т.КодВещества AND уб.КодТипаВыброса = т.КодТипаВыброса
        WHERE уб.НомерРазрешения = пп.НомерРазрешения 
          AND уб.Год = пп.Год 
          AND уб.Квартал = пп.Квартал
    )
    FROM ПлановыеПлатежи пп
    JOIN inserted i ON пп.НомерРазрешения = i.НомерРазрешения 
                    AND пп.Год = i.Год 
                    AND пп.Квартал = i.Квартал;
    
    PRINT 'Сумма платежа обновлена на основе новых данных о выбросах';
END;
GO